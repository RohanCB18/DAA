// Pick a random dataset
inline const Dataset& getRandomDataset(const std::vector<Dataset>& datasets) {
    static std::mt19937 rng(std::chrono::steady_clock::now().time_since_epoch().count());
    std::uniform_int_distribution<int> dist(0, datasets.size() - 1);
    return datasets[dist(rng)];
}

